#include<stdio.h>

int main(){
    int result = 5>3; // if the relation is true then output is 1 and if false then output is 0
    // True --> 1 and False -->0
    printf("%d \n",result);
    int result2 = 3<=3;     // ['=='] is "equal to" operator and ['='] is assignment operator 
    printf("%d",result2);
    return 0;
}
// For multiline comments use '/* */'
// '>' greater than operator
// '<' less than operator
// '=' assignment operator
// '==' equal to operator
// '!=' not equal to operator
// '<=' less than equal to operator
// '>=' greater than equal to operator
